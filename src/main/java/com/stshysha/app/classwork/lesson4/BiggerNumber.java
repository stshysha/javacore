public class BiggerNumber {
    public boolean calculateBigger(int x, int y) {
        if (x == y) {
            return true;
        } else {
            return false;
        }
    }

    public boolean calculateBigger2(int x, int y) {
        if (x > y) {
            return true;
        } else {
            return false;
        }
    }
}
